type MessageLocale = keyof typeof messages;

type TranslationLocale = SupportedLocale | MessageLocale;

const resolveMessage = (path: string, locale: MessageLocale): string => {
  const segments = path.split('.');
  let value: unknown = messages[locale];

  for (const segment of segments) {
    if (!value || typeof value !== 'object' || !(segment in value)) {
      value = undefined;
      break;
    }

    value = (value as Record<string, unknown>)[segment];
  }

  if (typeof value === 'string') {
    return value;
  }

  if (locale !== 'en') {
    return resolveMessage(path, 'en');
  }

  return path;
};

const resolveTranslationLocale = (value?: string | null): MessageLocale => {
  const base = (value ?? 'en').trim().toLowerCase().split('-')[0];
  return base in messages ? (base as MessageLocale) : 'en';
};

export const translate = (locale: TranslationLocale, path: string): string => {
  return resolveMessage(path, resolveTranslationLocale(locale));
};
